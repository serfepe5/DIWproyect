
$(document).ready(function(){
	$("#logojs").hover(function(){
        $("#logojs").css("height", "100px");
		$("#logojs").css("width", "100px");
		}, function(){
			$("#logojs").css("width", "80px");
            $("#logojs").css("height", "80px");
		});
});

